fn main() {
    let mut x = 3;
    println!("Number {}", x);
    x = 5; // don't change this line
    println!("Number {}", x);
}

// In Rust, variable bindings are immutable by default.
// We use a keyword 'mut' to make a variable bindings mulable instead.
